package kr.spring.alarm.vo;

public class AlarmVO {
	private int alarm_num;
	private int alarm_kind;
	private int mem_num;
	
	public int getAlarm_num() {
		return alarm_num;
	}
	public void setAlarm_num(int alarm_num) {
		this.alarm_num = alarm_num;
	}
	public int getAlarm_kind() {
		return alarm_kind;
	}
	public void setAlarm_kind(int alarm_kind) {
		this.alarm_kind = alarm_kind;
	}
	public int getMem_num() {
		return mem_num;
	}
	public void setMem_num(int mem_num) {
		this.mem_num = mem_num;
	}
}
